package com.fosun.data.service.model.vo;

public class CreditAdditionalInfo {
	
	private String monthDebt;
	private String productLimit;
	private String income;
	private String availableIncome;
	private String riskCoefficient;
	private String basicInterest;
	private String carPrice;
	
	
	
	public String getMonthDebt() {
		return monthDebt;
	}
	public void setMonthDebt(String monthDebt) {
		this.monthDebt = monthDebt;
	}
	public String getProductLimit() {
		return productLimit;
	}
	public void setProductLimit(String productLimit) {
		this.productLimit = productLimit;
	}
	public String getIncome() {
		return income;
	}
	public void setIncome(String income) {
		this.income = income;
	}
	public String getAvailableIncome() {
		return availableIncome;
	}
	public void setAvailableIncome(String availableIncome) {
		this.availableIncome = availableIncome;
	}
	public String getRiskCoefficient() {
		return riskCoefficient;
	}
	public void setRiskCoefficient(String riskCoefficient) {
		this.riskCoefficient = riskCoefficient;
	}
	public String getBasicInterest() {
		return basicInterest;
	}
	public void setBasicInterest(String basicInterest) {
		this.basicInterest = basicInterest;
	}
	public String getCarPrice() {
		return carPrice;
	}
	public void setCarPrice(String carPrice) {
		this.carPrice = carPrice;
	}
	
	
	
	
}
