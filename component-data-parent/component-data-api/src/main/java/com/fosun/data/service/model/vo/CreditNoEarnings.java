package com.fosun.data.service.model.vo;

public class CreditNoEarnings {
	private String capitalContributionPeriod;
	private String overseasInvestmentFlag;
	private String workYears;
	private String industry;
	
	
	
	public String getCapitalContributionPeriod() {
		return capitalContributionPeriod;
	}
	public void setCapitalContributionPeriod(String capitalContributionPeriod) {
		this.capitalContributionPeriod = capitalContributionPeriod;
	}
	public String getOverseasInvestmentFlag() {
		return overseasInvestmentFlag;
	}
	public void setOverseasInvestmentFlag(String overseasInvestmentFlag) {
		this.overseasInvestmentFlag = overseasInvestmentFlag;
	}
	public String getWorkYears() {
		return workYears;
	}
	public void setWorkYears(String workYears) {
		this.workYears = workYears;
	}
	public String getIndustry() {
		return industry;
	}
	public void setIndustry(String industry) {
		this.industry = industry;
	}



}
